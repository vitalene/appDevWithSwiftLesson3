//: ## Characters
//: Of course, text can contain more than just letters: there are punctuation marks, numbers, symbols, even spaces. All of these things are known as _characters_. _Strings_ are made up of _characters_.
//:
//: Letters are characters:
//: ## Aa Bb Cc Dd Ee
//: So are punctuation marks:
//: ## ! ? . , ; :
//: Numbers:
//: ## 1 2 3 4 5 6 7 8 9 0
//: Symbols:
//: ## # @ $ & © π * + - /
//: Even invisible characters like spaces and tabs:\
//: _You can’t see them, but they’re there._
//: ## `                         `
//:
//:
